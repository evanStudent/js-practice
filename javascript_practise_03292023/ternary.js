// ternary operator
// if predicate is true do_this, is false do_that
// (predicate) ? do_this : do_that

// difference between == and ===
console.log(2 == '2'); // expect: true
console.log(2 === '2'); // expect: false 

console.log(undefined == null) // expect: true 

console.log(NaN == NaN); // expect: false 

